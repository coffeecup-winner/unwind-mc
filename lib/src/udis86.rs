#[derive(Debug, Clone)]
pub struct Insn {
    pub address: u64,
    pub code: Mnemonic,
    pub length: u8,
    pub hex: String,
    pub assembly: String,
    pub operands: Vec<Operand>,
    pub prefix_rex: u8,
    pub prefix_segment: ud_type,
    pub prefix_operand_size: bool,
    pub prefix_address_size: bool,
    pub prefix_lock: u8,
    pub prefix_str: u8,
    pub prefix_rep: u8,
    pub prefix_repe: u8,
    pub prefix_repne: u8,
}

impl ToString for Insn {
    fn to_string(&self) -> String {
        self.assembly.clone()
    }
}

impl Insn {
    pub fn get_target_address(&self) -> u64 {
        let target_address = self.address + u64::from(self.length);
        (target_address as i64 + self.operands[0].get_i64()) as u64
    }
}

#[derive(Debug, Copy, Clone)]
pub struct Operand {
    pub type_: ud_type,
    pub size: u16,
    pub base: ud_type,
    pub index: ud_type,
    pub scale: u8,
    pub offset: u8,
    pub lvalue: ud_lval,
    pub oprcode: u8,
}

impl Operand {
    pub fn get_i64(&self) -> i64 {
        unsafe {
            match self.size {
                8 => i64::from(self.lvalue.sbyte),
                16 => i64::from(self.lvalue.sword),
                32 => i64::from(self.lvalue.sdword),
                64 => self.lvalue.sqword as i64,
                _ => panic!("Impossible"),
            }
        }
    }

    pub fn get_memory_offset(&self) -> i64 {
        unsafe {
            match self.offset {
                0 => 0,
                8 => i64::from(self.lvalue.sbyte),
                16 => i64::from(self.lvalue.sword),
                32 => i64::from(self.lvalue.sdword),
                64 => self.lvalue.sqword as i64,
                _ => panic!("Impossible"),
            }
        }
    }
}

