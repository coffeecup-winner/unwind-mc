use std::mem;

use libudis86_sys::*;

#[derive(Serialize, Deserialize)]
#[serde(default = "Disassembler::new")]
pub struct Disassembler {
    #[serde(skip)]
    ud: ud,
}

impl Disassembler {
    pub fn new() -> Self {
        unsafe {
            let mut ud: ud = mem::uninitialized();
            ud_init(&mut ud);
            ud_set_mode(&mut ud, 32);
            // Syntax generation in udis86 is bugged (incorrect vsnprintf calls)
            // Intel syntax translation is done manually below
            ud_set_syntax(&mut ud, None);
            Disassembler { ud }
        }
    }

    pub fn disassemble(&mut self, bytes: &[u8], pc: u64, offset: usize, length: usize) -> Vec<Insn> {
        let mut result = vec![];
        unsafe {
            ud_set_pc(&mut self.ud, pc);
            ud_set_input_buffer(&mut self.ud, bytes.as_ptr().add(offset), length);
            while ud_disassemble(&mut self.ud) != 0 {
                result.push(Insn {
                    address: ud_insn_off(&self.ud),
                    code: Mnemonic::from_ud_mnemonic_code(ud_insn_mnemonic(&self.ud)),
                    length: ud_insn_len(&self.ud) as u8,
                    // assembly: Self::print_intel(&self.ud, mnemonic, &operands),
                    operands: Self::get_operands(&mut self.ud),
                    prefix_segment: Reg::from_ud_type(mem::transmute(u32::from(self.ud.pfx_seg))),
                    prefix_operand_size: self.ud.pfx_opr == 0x66,
                    prefix_address_size: self.ud.pfx_adr == 0x67,
                    prefix_lock: self.ud.pfx_lock != 0,
                    prefix_str: self.ud.pfx_str != 0,
                    prefix_rep: self.ud.pfx_rep != 0,
                    prefix_repe: self.ud.pfx_repe != 0,
                    prefix_repne: self.ud.pfx_repne != 0,
                    br_far: self.ud.br_far != 0,
                    opr_mode: self.ud.opr_mode,
                });
            }
        }
        result
    }

    fn get_operands(ud: &mut ud) -> Vec<Operand> {
        let mut result = vec![];
        unsafe {
            let mut index = 0;
            let mut op = ud_insn_opr(ud, index);

            while !op.is_null() {
                result.push(Operand::from_ud_operand(ud.opr_mode as u16, *op));
                index += 1;
                op = ud_insn_opr(ud, index);
            }
        }
        result
    }
}

#[derive(Serialize, Deserialize, Debug, Clone)]
pub struct Insn {
    pub address: u64,
    pub code: Mnemonic,
    pub length: u8,
    pub operands: Vec<Operand>,
    pub prefix_segment: Reg,
    pub prefix_operand_size: bool,
    pub prefix_address_size: bool,
    pub prefix_lock: bool,
    pub prefix_str: bool,
    pub prefix_rep: bool,
    pub prefix_repe: bool,
    pub prefix_repne: bool,
    pub br_far: bool,
    pub opr_mode: u8,
}

impl Insn {
    pub fn get_target_address(&self) -> u64 {
        let target_address = self.address + u64::from(self.length);
        if let Operand::RelativeAddress(v) = self.operands[0] {
            (target_address as i64 + v) as u64
        } else {
            unreachable!()
        }
    }

    // The printing code is ported from udis86's syn-intel.c
    // The ported version is abridged, rare or non-x86 bits are removed
    pub fn assembly(&self) -> String {
        let mut result = String::new();

        if self.prefix_segment != Reg::NONE &&
            self.operands.len() > 1 &&
            !self.operands[0].is_mem() &&
            !self.operands[1].is_mem() {
            result += self.prefix_segment.to_str();
        }

        if self.prefix_lock {
            result += "lock ";
        }

        if self.prefix_rep {
            result += "rep ";
        } else if self.prefix_repe {
            result += "repe ";
        } else if self.prefix_repne {
            result += "repne ";
        }

        result += self.code.to_str();

        if self.operands.len() > 0 {
            let mut cast = false;
            result += " ";
            if self.operands[0].is_mem() {
                if self.operands.len() <= 1 ||
                    self.operands[1].is_imm() ||
                    self.operands[1].is_const() ||
                    self.operands[0].size() != self.operands[1].size() {
                    cast = true;
                } else if self.operands.len() > 1 {
                    if let Operand::Register(_, Reg::CL) = self.operands[1] {
                        match self.code {
                            Mnemonic::Ircl |
                            Mnemonic::Irol |
                            Mnemonic::Iror |
                            Mnemonic::Ircr |
                            Mnemonic::Ishl |
                            Mnemonic::Ishr |
                            Mnemonic::Isar => {
                                cast = true;
                            },
                            _ => {}
                        }
                    }
                }
            }
            result += &Self::print_operand(self, &self.operands[0], cast);
        }

        if self.operands.len() > 1 {
            let mut cast = false;
            result += ", ";
            if self.operands[1].is_mem() &&
                self.operands[1].size() != self.operands[0].size() {
                cast = true;
            }
            result += &self.print_operand(&self.operands[1], cast);
        }

        if self.operands.len() > 2 {
            let mut cast = false;
            result += ", ";
            if self.operands[2].is_mem() &&
                self.operands[2].size() != self.operands[1].size() {
                cast = true;
            }
            result += &self.print_operand(&self.operands[2], cast);
        }

        if self.operands.len() > 3 {
            result += ", ";
            result += &self.print_operand(&self.operands[3], false);
        }

        result
    }

    fn print_operand(&self, op: &Operand, cast: bool) -> String {
        let mut s = String::new();

        match op {
            Operand::Register(_, r) => {
                s += r.to_str();
            },
            &Operand::MemoryAbsolute(size, v) => {
                if cast {
                    s += &self.print_cast(size);
                }
                s += "[";
                if self.prefix_segment != Reg::NONE {
                    s += self.prefix_segment.to_str();
                    s += ":";
                }
                s += &format!("0x{:x}]", v);
            }
            &Operand::MemoryRelative(size, offset, base, index, scale, v) => {
                if cast {
                    s += &self.print_cast(size);
                }
                s += "[";
                if self.prefix_segment != Reg::NONE {
                    s += self.prefix_segment.to_str();
                    s += ":";
                }
                if base != Reg::NONE {
                    s += base.to_str();
                }
                if index != Reg::NONE {
                    if base != Reg::NONE {
                        s += "+";
                    }
                    s += index.to_str();
                    if scale != 0 {
                        s += &format!("*{}", scale);
                    }
                }
                if offset != 0 {
                    if v < 0 {
                        s += &format!("-0x{0:x}", -v);
                    } else {
                        s += &format!("+0x{0:x}", v);
                    }
                }
                s += "]";
            },
            Operand::ImmediateSigned(_, v) => {
                s += &format!("0x{:x}", v);
            }
            Operand::ImmediateUnsigned(_, v) => {
                s += &format!("0x{:x}", v);
            },
            Operand::RelativeAddress(v) => {
                let trunc_mask = 0xffffffffffffffff >> (64 - self.opr_mode);
                let pc = self.address + u64::from(self.length);
                let address = pc.wrapping_add(*v as u64) & trunc_mask;
                s += &format!("0x{:x}", address);
            },
            Operand::Pointer(size, seg, off) => {
                match size {
                    32 => {
                        s += &format!("word 0x{:x}0x{:x}", seg, off & 0xffff);
                    }
                    48 => {
                        s += &format!("dword 0x{:x}0x{:x}", seg, off);
                    }
                    _ => unreachable!(),
                }
            },
            &Operand::Const(size, v) => {
                if cast {
                    s += &self.print_cast(size);
                }
                s += &format!("{}", v);
            },
        }

        return s;
    }

    fn print_cast(&self, size: u16) -> String {
        let mut s = String::new();

        if self.br_far {
            s += "far ";
        }
        match size {
            8 => { s += "byte "; },
            16 => { s += "word "; },
            32 => { s += "dword "; },
            64 => { s += "qword "; },
            80 => { s += "tword "; },
            128 => { s += "oword "; },
            256 => { s += "yword "; },
            _ => {}
        }

        return s;
    }
}

#[derive(Serialize, Deserialize, Debug, PartialEq, Copy, Clone)]
pub enum Operand {
    Register(u16, Reg),
    MemoryAbsolute(u16, u64),
    MemoryRelative(u16, u8, Reg, Reg, u8, i64),
    Pointer(u16, u16, u32),
    ImmediateUnsigned(u16, u64),
    ImmediateSigned(u16, i64),
    RelativeAddress(i64),
    Const(u16, u64),
}

impl Operand {
    pub fn from_ud_operand(mode: u16, operand: ud_operand) -> Operand {
        use self::Operand::*;
        match operand.otype {
            ud_type::UD_OP_REG => Register(operand.size, Reg::from_ud_type(operand.base)),
            ud_type::UD_OP_MEM => {
                let base = Reg::from_ud_type(operand.base);
                let index = Reg::from_ud_type(operand.index);
                if base == Reg::NONE && index == Reg::NONE {
                    assert!(operand.scale == 0 && operand.offset != 8);
                    MemoryAbsolute(operand.size,
                        Self::get_off_u64(operand.offset, operand.lval))
                } else {
                    assert!(operand.offset != 64);
                    MemoryRelative(operand.size, operand.offset, base, index, operand.scale,
                        Self::get_off_i64(operand.offset, operand.lval))
                }
            },
            ud_type::UD_OP_PTR => Pointer(
                operand.size,
                unsafe { operand.lval.ptr.seg },
                unsafe { operand.lval.ptr.off }),
            ud_type::UD_OP_IMM => {
                if operand._oprcode == 46 /* OP_sI */ && operand.size != mode {
                    // Sign expand
                    if operand.size != 8 {
                        assert!(operand.size == 32);
                    }
                    let mut v = Self::get_imm_i64(operand.size, operand.lval) as u64;
                    if mode < 64 {
                        v = v & (0xffffffffffffffff >> (64 - mode));
                    }
                    ImmediateSigned(operand.size, v as i64)
                } else {
                    ImmediateUnsigned(operand.size,
                        Self::get_imm_u64(operand.size, operand.lval))
                }
            },
            ud_type::UD_OP_JIMM => RelativeAddress(
                Self::get_imm_i64(operand.size, operand.lval)),
            ud_type::UD_OP_CONST => Const(operand.size, unsafe { operand.lval.uqword }),
            _ => panic!("Register used as operand type"),
        }
    }

    pub fn is_mem(&self) -> bool {
        match self {
            Operand::MemoryAbsolute(_, _)
            | Operand::MemoryRelative(_, _, _, _, _, _) => true,
            _ => false,
        }
    }

    pub fn is_imm(&self) -> bool {
        match self {
            Operand::ImmediateSigned(_, _)
            | Operand::ImmediateUnsigned(_, _) => true,
            _ => false,
        }
    }

    pub fn is_reladdr(&self) -> bool {
        match self {
            Operand::RelativeAddress(_) => true,
            _ => false,
        }
    }

    pub fn is_const(&self) -> bool {
        match self {
            Operand::Const(_, _) => true,
            _ => false,
        }
    }

    pub fn size(&self) -> u16 {
        match self {
            &Operand::Register(s, _) => s,
            &Operand::MemoryAbsolute(s, _) => s,
            &Operand::MemoryRelative(s, _, _, _, _, _) => s,
            &Operand::Pointer(s, _, _) => s,
            &Operand::ImmediateUnsigned(s, _) => s,
            &Operand::ImmediateSigned(s, _) => s,
            Operand::RelativeAddress(_) => unreachable!(),
            &Operand::Const(s, _) => s,
        }
    }

    fn get_imm_u64(size: u16, lvalue: ud_lval) -> u64 {
        unsafe {
            match size {
                8 => u64::from(lvalue.ubyte),
                16 => u64::from(lvalue.uword),
                32 => u64::from(lvalue.udword),
                64 => lvalue.uqword,
                _ => 0,
            }
        }
    }

    fn get_imm_i64(size: u16, lvalue: ud_lval) -> i64 {
        unsafe {
            match size {
                8 => i64::from(lvalue.sbyte),
                16 => i64::from(lvalue.sword),
                32 => i64::from(lvalue.sdword),
                64 => lvalue.sqword,
                _ => 0,
            }
        }
    }

    fn get_off_u64(offset: u8, lvalue: ud_lval) -> u64 {
        unsafe {
            match offset {
                0 => 0,
                8 => u64::from(lvalue.ubyte),
                16 => u64::from(lvalue.uword),
                32 => u64::from(lvalue.udword),
                64 => lvalue.uqword,
                _ => unreachable!(),
            }
        }
    }

    fn get_off_i64(offset: u8, lvalue: ud_lval) -> i64 {
        unsafe {
            match offset {
                0 => 0,
                8 => i64::from(lvalue.sbyte),
                16 => i64::from(lvalue.sword),
                32 => i64::from(lvalue.sdword),
                64 => lvalue.sqword,
                _ => unreachable!(),
            }
        }
    }
}

